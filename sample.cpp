#include <iostream>
using namespace std;

int main() {
    int num1 = 15;  // first integer
    int num2 = 22;  // second integer
    int sum;

    // Calculate sum
    sum = num1 + num2;

    // Output result
    cout << "First integer: " << num1 << endl;
    cout << "Second integer: " << num2 << endl;
    cout << "The sum is " << sum << "." << endl;

    return 0;
}
